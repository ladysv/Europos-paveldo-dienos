                <h2>Apie Europos paveldo dienas</h2>
                <p>Idėja pradėti rengti paveldo dienas gimė Prancūzijoje dar 1984 metais, jos iniciatorius buvo Prancūzijos kultūros ministras Jack Lang. Per keletą metų šis renginys išplito po daugelį vakarų Europos šalių ir pasidarė toks populiarus, jog nuo 1991 metų Europos Tarybai inicijavus ir parėmus Europos Komisijai, visoje Europoje imta rengti Europos paveldo dienas.Lietuva prie „Europos paveldo dienos” programos prisijungė 1994 m. Europos paveldo dienų metu Europos valstybėse pagal tų metų pasirinktą temą organizuojami įvairaus pobūdžio nemokami renginiai, skirti visuomenei, siekiant ją supažindinti su kultūros paveldu, parodyti tai, kas paprastai yra visuomenei neprieinama, aktualizuojant vienokią ar kitokią kultūros paveldo sritį. Labiausiai tikimasi šia programa sudominti moksleivius ir paskatinti juos domėtis tiek savo šalies kultūra, istorija, tiek ir kitų Europos šalių kultūros paveldu.</p>
